package model;


public class Pet {
	
	
	private String id, name, birthdate, gender, typePet;

	
	public Pet(String id, String name, String birthdate, String gender, String typePet) {
		super();
		this.id = id;
		this.name = name;
		this.birthdate = birthdate;
		this.gender = gender;
		this.typePet = typePet;
	} 
	
	

}
