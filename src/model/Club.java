package model;

/**
 * Class that represents a club.
 * @author emanuelumana
 *
 */
public class Club {
	
	/**
	 * the attributes of the class
	 */
	private String name, date, id, typePet;

	/**
	 * Constructor of the Club class.
	 * @param name
	 * @param date
	 * @param id
	 * @param typePet
	 */
	public Club(String name, String date, String id, String typePet) {
		super();
		this.name = name;
		this.date = date;
		this.id = id;
		this.typePet = typePet;
	}
	
	
	
}
